#fatiamento - string

frase = "Josué Batista dos Santos"
print(frase[0:8])
print(frase[0:8:2]) #pulando de 2 em 2 igual range
print(frase[-1]) #último caractere
print(frase[:5]) #0 a 5
print(frase[5:]) #5 ao fim
print(len(frase)) #quantidade de caractéres
print(frase.count('o')) #quantidade de caractéres iguais